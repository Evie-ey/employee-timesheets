const mongoose = require('mongoose');
const schema = mongoose.Schema;

const EmployeeSchema = new Schema({
  username: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});

module.exports = Employee = mongoose.model('Employee', EmployeeSchema)